# ===========================================================================
# Shared shell aliases — sourced by both .bashrc and .zshrc
# Works across Linux, macOS, and WSL
# ===========================================================================

# ---------------------------------------------------------------------------
# Navigation
# ---------------------------------------------------------------------------
alias ..='cd ..'
alias ...='cd ../..'
alias ....='cd ../../..'

# ---------------------------------------------------------------------------
# Safety
# ---------------------------------------------------------------------------
alias rm='rm -i'
alias cp='cp -i'
alias mv='mv -i'

# ---------------------------------------------------------------------------
# Git shortcuts
# ---------------------------------------------------------------------------
alias gs='git status'
alias gd='git diff'
alias gl='git log --oneline --graph --decorate -20'
alias gp='git push'
alias gc='git commit'

# ---------------------------------------------------------------------------
# Misc
# ---------------------------------------------------------------------------
alias path='echo -e ${PATH//:/\\n}'
alias reload='exec $SHELL -l'
